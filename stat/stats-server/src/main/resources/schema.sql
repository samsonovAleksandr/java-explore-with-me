CREATE TABLE IF NOT EXISTS endpoint_hit (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
  app VARCHAR(255) NOT NULL,
  uri VARCHAR(512) NOT NULL,
  ip VARCHAR(255) NOT NULL,
  timestamp TIMESTAMP
);

CREATE TABLE IF NOT EXISTS view_stats (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
  app VARCHAR(255),
  uri VARCHAR(512),
  hits INTEGER(64),
);
